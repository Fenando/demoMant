    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (this.jRadioButtonTodas.isSelected()) {
            new BLL.Reporte().ReportePorAreaDate(this.areaK,this.tipoK,
                    this.jDateChooser1.getDate(),
                this.jDateChooser2.getDate());
            dispose();
        }
        if (this.jRadioButtonNoRealizadas.isSelected()) {
            new BLL.Reporte().ReportePorAreaDateNoRealizada(this.areaK,this.tipoK,
                    this.jDateChooser1.getDate(),
                this.jDateChooser2.getDate());
            dispose();
        }
        if (this.jRadioButtonRealizadas.isSelected()) {
            new BLL.Reporte().ReportePorAreaDateRealizada(this.areaK,this.tipoK,
                    this.jDateChooser1.getDate(),
                this.jDateChooser2.getDate());
            dispose();
        }
        if (this.jRadioButtonRealizadas1.isSelected()) {
            new BLL.Reporte().ReportePorAreaCantidadOt(this.areaK,this.tipoK,
                    this.jDateChooser1.getDate(),
                this.jDateChooser2.getDate());
            dispose();
        }
       
        
    }//GEN-LAST:event_jButton1ActionPerformed

